export const ALL_STOCKS: StockMeta[] = [
  ...NSE_STOCKS,
  ...NASDAQ_STOCKS,
  ...NYSE_STOCKS,
]

export function findStock(symbol: string): StockMeta | undefined {
  return ALL_STOCKS.find(s => s.sym.toUpperCase() === symbol.toUpperCase())
}

export function detectExchange(symbol: string): Exchange {
  const stock = findStock(symbol)
  if (stock) return stock.exchange
  if (/^[A-Z&-]{5,10}$/.test(symbol)) return "NSE"
  return "NASDAQ"
}

export function searchStocks(query: string, limit = 8): StockMeta[] {
  const q = query.toUpperCase().trim()
  if (!q) return []
  return ALL_STOCKS.filter(s =>
    s.sym.startsWith(q) ||
    s.sym.includes(q) ||
    s.name.toUpperCase().includes(q)
  ).slice(0, limit)
}

export function getByExchange(exchange: Exchange): StockMeta[] {
  return ALL_STOCKS.filter(s => s.exchange === exchange)
}

export function getByIndex(index: string): StockMeta[] {
  return ALL_STOCKS.filter(s => s.index?.includes(index))
}

export function getBySector(sector: string): StockMeta[] {
  return ALL_STOCKS.filter(s => s.sector?.toLowerCase().includes(sector.toLowerCase()))
}

